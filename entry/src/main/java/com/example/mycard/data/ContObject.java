package com.example.mycard.data;

public class ContObject {
    private String name;
    private String pic;
    private String pubTime;
    private String interactionNum;

    private String contId;
    private String forwordType;
    private String link;
    private String isOutForword;
    private String referer;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public String getPubTime() {
        return pubTime;
    }

    public void setPubTime(String pubTime) {
        this.pubTime = pubTime;
    }

    public String getInteractionNum() {
        return interactionNum;
    }

    public void setInteractionNum(String interactionNum) {
        this.interactionNum = interactionNum;
    }

    public String getContId() {
        return contId;
    }

    public void setContId(String contId) {
        this.contId = contId;
    }

    public String getForwordType() {
        return forwordType;
    }

    public void setForwordType(String forwordType) {
        this.forwordType = forwordType;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getIsOutForword() {
        return isOutForword;
    }

    public void setIsOutForword(String isOutForword) {
        this.isOutForword = isOutForword;
    }

    public String getReferer() {
        return referer;
    }

    public void setReferer(String referer) {
        this.referer = referer;
    }
}


